package DailyPractice28112020;

public class OddEven {
	
	public static void main(String args[])
	{
		for(int i = 0; i<=100; i++)
		{
			if(i % 2 == 0)
			{
				System.out.println("Even No Is: " +i);
			}
			
			else
			{
				System.out.println("Odd No. is: " +i);
			}
		}
	}

}
